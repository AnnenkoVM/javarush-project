package com.javarush.task.pro.task04.task0404;

/* 
Заполненный прямоугольник
*/

public class Solution {
    public static void main(String[] args) {
        int l = 0;


        while (!(l == 5)) {
            l++;
            int r = 0;

            while (!(r == 10)) {
                System.out.print("Q");
                r++;
            }
            System.out.println();
        }
    }
}